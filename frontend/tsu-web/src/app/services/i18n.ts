@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly storageKey = 'tsu-web-language';
  protected readonly dictionary = UI_TRANSLATIONS;
  readonly language = signal<AppLanguage>(this.readSavedLanguage());

  constructor() {
    effect(() => {
      const lang = this.language();
      localStorage.setItem(this.storageKey, lang);
      document.documentElement.lang = lang;
    });
  }

  setLanguage(language: AppLanguage) {
    this.language.set(language);
  }

  t(key: string): string {
    const lang = this.language();
    const value = key.split('.').reduce<unknown>((current, part) => {
      if (current && typeof current === 'object' && part in current) {
        return (current as Record<string, unknown>)[part];
      }

      return undefined;
    }, this.dictionary[lang]);

    return typeof value === 'string' ? value : key;
  }

  getHighlightMetrics() {
    this.language();
    return HIGHLIGHT_METRICS[this.language()];
  }

  getQuickLinks() {
    this.language();
    return QUICK_LINKS[this.language()];
  }

  getNewsItems() {
    this.language();
    return NEWS_ITEMS[this.language()];
  }

  getEventItems() {
    this.language();
    return EVENT_ITEMS[this.language()];
  }

  private readSavedLanguage(): AppLanguage {
    const savedLanguage = localStorage.getItem(this.storageKey);
    return savedLanguage === 'ka' ? 'ka' : 'en';
  }
}
